package a_march.ex_10_for_loop;

public class Lab103ForIf {
    public static void main(String[] args) {

        for (int i = 0; i < 20; i++) {
            if (i == 15) {
                System.out.println("Yash will get gift");
            }
            else
                System.out.println("No gift");
        }
    }
}
