package march.ex_09_Switch;

public class Lab096LabInterview6 {
    public static void main(String[] args) {

        char code = 'C';
        switch (code) {
            default:
                System.out.println("Hello");
                break;
            case 'A':
                System.out.println(10);
                break;
            case 'B':
                System.out.println(9);
                break;
        }
    }
}
